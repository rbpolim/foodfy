const ShowAndHide = {
	ingredients() {
		var x = document.getElementById('ingredients');
		if (x.style.display === 'none') {
			x.style.display = 'block';
		} else {
			x.style.display = 'none';
		}
	},
	prepare() {
		var x = document.getElementById('prepare');
		if (x.style.display === 'none') {
			x.style.display = 'block';
		} else {
			x.style.display = 'none';
		}
	},
	plus() {
		var x = document.getElementById('plus');
		if (x.style.display === 'none') {
			x.style.display = 'block';
		} else {
			x.style.display = 'none';
		}
	}
};
